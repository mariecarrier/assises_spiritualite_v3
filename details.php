<h2>Description des ateliers</h2>

<h4 style=" font-size: 16px;">La philosophie, un lieu de consolation</span></h4>
</h3>
<p><strong>Jean Proulx :</strong> Professeur de philosophie au cégep, conseiller aux politiques ministérielles en éducation, secrétaire général du Conseil supérieur de l’éducation du Québec. Il a écrit plusieurs livres favorisant la rencontre de la philosophie, de la science et de la spiritualité. <br><br>
<strong>Le Dieu cosmique, Le jour<br>
La chorégraphie divine, Septentrion<br>
Écoute en toi cette sublime musique, Médiaspaul<br></strong>
<br>
La philosophie est un lieu de consolation, d’espoir et de réconfort quand elle apprend à lire les signes de sens présents dans un univers ordonné, dans la force créatrice de la vie et dans ce qu’il y a de meilleur en l’être humain. 
La philosophie est aussi un lieu de consolation lorsqu’elle réussit à intégrer profondément, dans sa quête de sens, des apports significatifs de la science, de la spiritualité et de l’art.
J’ai proposé de telles réflexions, notamment dans La chorégraphie divine,  dans En quête de sens.(Le Jour, 2011) et dans Écoute en toi cette sublime musique.  (Médiaspaul, 2014).
</p><br><br>



<h4 style="font-size: 16px;">Soigner ou consoler</span></h4>
<p><strong>Jean-Pierre Routy :</strong> Médecin, hémato-oncologie au centre de santé universitaire de McGill (CUSM). Son implication dans les soins et la recherche sur les personnes infectées par le VIH, l’a obligé à souvent être confronté aux enjeux souvent opposés entre les soins aux individus et à la communauté.       
<strong>Ce que le Sida a changé, Héliotrope</strong></p>

<p>“Quand on est heureux, il reste beaucoup à faire: consoler les autres ». Cette citation de Jules Romain montre que la personne qui console doit être en état de le faire. Le nouveau modèle des centres de soins au Québec le C.A.R.E. Connecter, Agir, Respecter, Expérimenter, repose sur l’idée de prendre soin aussi de soi pour mieux prendre soin des patients. Ce modèle pose les limites du soin et peut ouvrir sur la consolation. Consoler, dans ce cas peut être possible par un acte de solidarité, de foi, de quête de sens, et tend à avoir comme perspective l’art, la poésie et le silence.</p><br><br>



<h4 style="font-size: 16px;">Dieu qui console</span></h4>

<p><strong>Edouard Chatov :</strong> Augustin de l'Assomption, responsable des programmes des conférences au Centre "Culture et Foi" du Montmartre, Québec</p>

<p>Dans notre vie quotidienne nous essayons de nous  consoler les uns, les autres pour donner à  la vie un élan nouveau. Il y a des jours où l’on réussit à poser des gestes de compassion et d’ affection mutuelle, et d’autres  où nous échouons. Même dans les moments  difficiles, nous espérons la consolation de Dieu. Comment envisager, comprendre et accueillir la consolation de Dieu dans notre quotidien.</p><br>


<h4 style="font-size: 16px;">Job, consoler l’inconsolable</span></h4>

<p><strong>Sébastien Doane : </strong>Communicateur passionné de la Bible, auteur de quelques livres chez Novalis, il est responsable de la rédaction du site www.interbible.org et complète un doctorat en études bibliques à l’Université Laval.</p>

Mais d’où vient la femme de Cain, Novalis
Lexique sympathique de la Bible, Novalis 

Le livre de Job met en scène un homme intègre, juste qui souffre apparemment sans raison. Cette souffrance est le lieu d’une réflexion théologique importante. Dans cet atelier, les participants pourront explorer le livre de Job pour voir les diverses réactions à la souffrance qu’il contient. Après une mise en contexte historique et littéraire, nous passerons en revue les attitudes de Job, de ses amis ainsi que la réaction de Dieu. Dans son expérience de la souffrance et grâce à son dialogue avec Dieu, Job passe de l’incompréhension à la contemplation. Nous verrons comment ce cheminement peut nous inspirer aujourd’hui.
Voir la souffrance autrement
Jean-Marc Barreau : prêtre, aumônier aux soins palliatifs de l’hôpital Marie-Clarac, Montréal, Docteur Ph.D et professeur en théologie (Montréal et Paris).
Jean-Paul II : le saint de la Nouvelle Évangélisation, Salvator
François et la miséricorde, à paraître Médiaspaul
Jean-Marc Barreau nous invitera à regarder la problématique de « La souffrance » à la lumière de la miséricorde. Dans un regard d’analyse enrichi de son expérience en soins palliatifs, le pasteur et le théologien balaieront résolument tout regard mortifère sur la souffrance et nous initieront  à « Voir la souffrance autrement ».
Cette vision humaniste met au premier plan le bien-fondé des « Soins palliatifs » tout en pointant du doigt certains sophismes édictés par nos cultures occidentales.
Peut-on consoler quand la vie n’a plus de sens; le suicide

Andrée Quiviger , : Essayiste, psycho-éducatrice  et bénévole à Suicide Action Montréal depuis une dizaine d’années.  Andrée Quiviger consacre actuellement sa réflexion et son action professionnelles aux personnes en deuil à la suite d’un suicide.
Ne meurs pas, Bayard Canada 

Il n’y a guère de repos pour les proches d’une personne suicidée. Des images morbides défilent dans leur pensée; ils cherchent désespérément des réponses  introuvables; leur mémoire s’acharne sur mille signaux inaperçus. Une imparable culpabilité empoisonne leur conscience. Une profonde colère pourrait les dévaster. Ils sont en perte de sens. Leur besoin de consolation est immense mais, comme le prêtre et le lévite de la parabole, les consolateurs sont souvent tentés de traverser de l’autre côté du chemin. 






Quand l’art devient consolation

Mario Paquet : Animateur à Radio-Canada depuis plus de 30 ans, principalement aux émissions de musique classique. 
Lexique sympathique de la vie spirituelle, Novalis

Qui dans sa vie n’a pas à un moment où à un autre puiser dans un livre, une musique, une mélodie, un poème une force nouvelle qui lui a permis d’adoucir l’épreuve? L’art sous toutes ses formes peut s’avérer un antidépresseur naturel et nous aider à rebondir et à trouver une forme de réconfort dans nos moments de détresse et de souffrance en nous permettant une plongée dans un autre univers qui nous permet de sortir de nous-mêmes et de nous faire voir les choses autrement. 

