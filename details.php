<h3 style="text-align:center; font-weight:bold;">LES POUVOIRS DE LA CONSOLATION<br></h3>
<h3  style="text-align:center; font-style:italic;"><em>Peut-on consoler quand la vie n’a plus de sens ?</em></h3>
<br>
<p>Pour sa troisième édition, les Assises de la spiritualité ont choisi d’explorer les différentes approches qu’offrent  la philosophie, la médecine, la religion, la psychologie et les arts pour aider la personne à surmonter la maladie, la mort, le deuil et le mal de vivre qui, aujourd’hui, semblent devenus incongrus, voire intolérables. Gabriel Ringlet, auteur à succès, entre autres, de L’Évangile d’un libre-penseur et de L’éloge de la fragilité, nous transmettra tout au long de ces deux journées le fruit de ses nombreuses expériences en accompagnement. Des intervenants, aux larges compétences aussi bien dans les domaines scientifique que spirituel, animeront des ateliers pour approfondir ce thème de la consolation.</p><br><br>
<img src="/assises/img/portrait.jpg"/ class="invite">
<h3><strong>Gabriel Ringlet</strong><br>
<span style=" font-size: 16px;">Conférencier invité</span></h3>
</h3>
<p>
<strong>Gabriel RINGLET</strong>  est né à Pair-Clavier, dans le Condroz liégeois (Belgique), le 16 avril 1944.
Professeur émérite à l'Université Catholique de Louvain (Louvain-la-Neuve) il y a présidé le Département de communication et dirigé la section de journalisme. Après avoir été vice-recteur aux Affaires étudiantes de 1988 à 2001, il est devenu le prorecteur chargé des Affaires régionales, de la culture et de la communication.</p><br>
<p>Prêtre, écrivain et théologien, il a publié entre autres Ces chers disparus (1992), Un peu de mort sur le visage (1997), L’Évangile d’un libre penseur (1998), La résistance intérieure (1998), Éloge de la fragilité  (troisième édition 2000), Ma part de gravité (2002), Et je serai pour vous un enfant laboureur (2006), Ceci est ton corps (2008), Entre toutes les femmes, dialogue avec la chanteuse Mannick (2011) et Effacement de Dieu. La voie des moines-poètes. (2013).
Il est membre de l’Académie Royale de Langue et de Littérature françaises de Belgique.</p><br>
<p>Particulièrement attentif aux questions éthiques et spirituelles qui se posent en fin de vie, il accompagne régulièrement la réflexion d’équipes soignantes et s’engage dans le domaine des soins palliatifs.</p><br>
<p>Depuis son lieu de « résistance poétique » au Prieuré de Malèves-Sainte-Marie en Brabant wallon, il encourage de nouveaux rendez-vous entre chrétiens et laïques, accueille des artistes et des écrivains et invite chacun à parcourir les chemins de l’imaginaire.</p>



